    public void calcularSalario() {
        if (txtHoursWorkings.getText().trim().isEmpty() || txtName.getText().trim().isEmpty() || txtValueHour.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ingrese todos los datos", "CALCULAR", JOptionPane.WARNING_MESSAGE);
            txtName.requestFocus();
        } else {
            double valorXHora = Double.parseDouble(txtValueHour.getText());
            double salarioBruto = calcularSalarioBruto(valorXHora);
            double retencion = calcularRetencion(salarioBruto);
            double salarioNeto = calcularSalarioBruto(valorXHora) - retencion;
            infoBruto.setText("$" + salarioBruto);
            infoRetencion.setText("$" + retencion);
            infoNeto.setText("$" + salarioNeto);
            infoName.setText(txtName.getText());
        }
    }

    public double calcularSalarioBruto(double valorXHora) {
        if (Double.parseDouble(txtHoursWorkings.getText()) > 40) {
            valorXHora = ((valorXHora * 35 / 100) + valorXHora);
            infoValorHora.setText("$" + valorXHora);
        }
        infoValorHora.setText("$" + valorXHora);
        return Integer.parseInt(txtHoursWorkings.getText()) * valorXHora;
    }

    public double calcularRetencion(double salarioBruto) {
        double retencion = 0;
        if (salarioBruto < 1200000) {
            retencion = (salarioBruto * 10 / 100);
        }

        if (salarioBruto >= 1200000 & salarioBruto < 2000000) {
            retencion = (salarioBruto * 20 / 100);
        }

        if (salarioBruto > 2000000) {
            retencion = (salarioBruto * 25 / 100);
        }
        return retencion;
    }

    public void clean() {
        txtName.setText("");
        txtHoursWorkings.setText("");
        txtValueHour.setText("");
        infoName.setText("");
        infoBruto.setText("");
        infoRetencion.setText("");
        infoValorHora.setText("");
        infoNeto.setText("");
        txtName.requestFocus();
        JOptionPane.showMessageDialog(null, "Datos limpiados", "LIMPIAR", JOptionPane.INFORMATION_MESSAGE);
    }

